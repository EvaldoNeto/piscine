int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if ((unsigned int)*s1 != (unsigned int)*s2 && n > 0)
		return (*s1 - *s2);
	else if ((*s1 == '\0' && *s2 == '\0') || n == 0)
		return (0);
	else
		return (ft_strncmp(s1 + 1, s2 + 1, n - 1));
}
