int		ft_sqrt(int nb)
{
	int temp;

	if (nb >= 0)
	{
		temp = nb;
		while (temp > 0 && (nb / temp != temp || nb % temp != 0))
		{
			temp--;
		}
		return (temp);
	}
	else
		return (0);
}
