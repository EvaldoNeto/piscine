int		ft_is_prime(int nb)
{
	int temp;

	temp = 2;
	if (nb > 1 || nb < -1)
	{
		if (nb < 0)
		{
			nb = -nb;
		}
		while (nb % temp != 0 && temp <= nb / 2)
		{
			temp++;
		}
		return (temp <= nb / 2) ? 0 : 1;
	}
	else
		return (0);
}
